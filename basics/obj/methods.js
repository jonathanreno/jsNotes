// in objects, functions can be added ass properties... called 'methods'

// this is an example of a function created OUTSIDE of an obj being used as a method

const add = (x, y) => {
  return x + y;
};

const math = {
  add,
};

const addNums = math.add(5, 7);

console.log(addNums); // output= 12
////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////
// This is a basic example of creating a method INSIDE of an object

const moreMath = {
  multiply: (x, y) => {
    return x * y;
  },
};

const mulNums = moreMath.multiply(3, 5);

console.log(mulNums); // output=15
////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////
// You can also use method shorthand syntax, where you can leave out the key (key:) syntax altogether

const yetMoreMath = {
  subtract(x, y) {
    return x - y;
  },
};

const subNums = yetMoreMath.subtract(10, 5);

console.log(subNums); // ouput= 5
